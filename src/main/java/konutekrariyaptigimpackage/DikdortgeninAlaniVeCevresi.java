package konutekrariyaptigimpackage;

public class DikdortgeninAlaniVeCevresi {

    public static double dikdortgeninAlani(double en, double boy) {
      double dAlani = en * boy;
      return dAlani;


    }

    public static double dikdortgeninCevresi(double en, double boy) {
        double dCevresi = 2*(en+boy);
        return dCevresi;
    }


}
